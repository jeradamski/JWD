public class AsciiCode {
    public static void main(String[] args) {
        char a = 'A';

        System.out.println("    "+a+"   ");
        System.out.println("   "+ a +" "+ a + "   ");
        System.out.println("  " + a + "  " + "  " + a + "  " );
        System.out.println(a + "  " + a + "  " + a + "  " + a);


        System.out.println(a+""+a);
        System.out.println(a+a);

    }
}
